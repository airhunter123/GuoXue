package PageControl.WelcomePage;

public class WelcomeVideo {
    String[] videoTopic ;
    String[] videoTitle ;
    String[] videoHref ;
    String[] pic ;
    public String[] getVideoHref() {
        return videoHref;
    }

    public void setVideoHref(String[] videoHref) {
        this.videoHref = videoHref;
    }

    public String[] getVideoTitle() {
        return videoTitle;
    }

    public void setVideoTitle(String[] videoTitle) {
        this.videoTitle = videoTitle;
    }

    public String[] getVideoTopic() {
        return videoTopic;
    }

    public void setVideoTopic(String[] videoTopic) {
        this.videoTopic = videoTopic;
    }

    public String[] getPic() {
        return pic;
    }

    public void setPic(String[] pic) {
        this.pic = pic;
    }
    
}
